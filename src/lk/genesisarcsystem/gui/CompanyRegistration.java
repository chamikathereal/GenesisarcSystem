    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
         if (jTable1.getSelectedRow() == -1) {
             JOptionPane.showMessageDialog(this, "Please Select row", "Warning", JOptionPane.WARNING_MESSAGE);
         }else{
             String name = jTextField3.getText();
            String hotline = jTextField4.getText();

            int row = jTable1.getSelectedRow();
            String id = String.valueOf(jTable1.getValueAt(row, 0));

            String selectedName = String.valueOf(jTable1.getValueAt(row, 1));
            String selectedHotline = String.valueOf(jTable1.getValueAt(row, 2));

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter company name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (hotline.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter company hotline", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (!hotline.matches("^0((11)|(2(1|[3-7]))|(3[1-8])|(4(1|5|7))|(5(1|2|4|5|7))|(6(3|[5-7]))|([8-9]1))[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Invalid company hotline", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (selectedHotline.equals(hotline) && selectedName.equals(name)) {
                JOptionPane.showMessageDialog(this, "Please change name or hotline", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                try {

                    ResultSet resultSet = MYSQL.execute("SELECT * FROM `company` WHERE (`name`='" + name + "' OR `hotline`='" + hotline + "') AND `id`!='" + id + "'");

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "Company name or hotline alredy added", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        MYSQL.execute("UPDATE `company` SET `name`='" + name + "', `hotline`='" + hotline + "' WHERE  `id`='" + id + "'");

                        loadCompanies();
                        reset();
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
         }
     reset();
    }//GEN-LAST:event_jButton4ActionPerformed

