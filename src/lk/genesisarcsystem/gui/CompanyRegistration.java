    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String name = jTextField3.getText();
        String hotline = jTextField4.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter company name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (hotline.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter company hotline", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!hotline.matches("^0((7(1|2|4|5|6|7|8))|(11)|(2(1|[3-7]))|(3[1-8])|(4(1|5|7))|(5(1|2|4|5|7))|(6(3|[5-7]))|([8-9]1))[0-9]{7}$")) {
        JOptionPane.showMessageDialog(this, "Invalid company hotline", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet resultSet = MYSQL.execute("SELECT * FROM `company` WHERE `name`='" + name + "' OR `hotline`='" + hotline + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Company name or hotline alredy added", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MYSQL.execute("INSERT INTO `company`(`name`,`hotline`) VALUES('" + name + "','" + hotline + "')");

                    loadCompanies();
                    reset();
                }
              
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
        

    }//GEN-LAST:event_jButton3ActionPerformed

