    private void UserType(){
         try {

            ResultSet resultSet = MYSQL.execute("SELECT * FROM user_type");

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                String id = resultSet.getString("id");        // Get the id
                String city = resultSet.getString("type");  // Get the method
                v.add(id + " - " + city);  // Concatenate both fields into a string and add it to the Vector

            }

            DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBox1.getModel();
            model.removeAllElements();

            model.addAll(v);
            jComboBox1.setSelectedIndex(0);

        } catch (Exception e) {
            e.printStackTrace();

        }
    }

