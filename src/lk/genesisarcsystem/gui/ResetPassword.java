    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String oldusername = jTextField1.getText();  // Old username field
        String newusername = jTextField2.getText();  // New username field
        char[] passwordOldArr = jPasswordField1.getPassword();  // Old password field
        String oldPassword = new String(passwordOldArr);  // Convert old password to string
        char[] passwordNewArr = jPasswordField2.getPassword();  // New password field
        String newPassword = new String(passwordNewArr);  // Convert new password to string
        String userType = (String) jComboBox1.getSelectedItem();  // Get user type from ComboBox
        int userType_id = jComboBox1.getSelectedIndex();
        // Validation checks
        if(oldusername.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your Old UserName", "Empty User Name", JOptionPane.ERROR_MESSAGE);
        } else if(newusername.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your New UserName", "Empty User Name", JOptionPane.ERROR_MESSAGE);
        } else if(newusername.equals(oldusername)){
            JOptionPane.showMessageDialog(this, "New UserName cannot be the same as the Old UserName", "User Name Error", JOptionPane.ERROR_MESSAGE);
        } else if(oldPassword.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your Old Password", "Empty Password", JOptionPane.ERROR_MESSAGE);    
        } else if(newPassword.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your New Password", "Empty Password", JOptionPane.ERROR_MESSAGE);    
        } else if(newPassword.equals(oldPassword)){
            JOptionPane.showMessageDialog(this, "New Password cannot be the same as the Old Password", "Password Error", JOptionPane.ERROR_MESSAGE);
        } else {
    // Proceed with resetting username and password
                try {
                     // First, check if the old username and password are valid
                     ResultSet resultSet = MYSQL.execute(
                         "SELECT * FROM `user` WHERE `username` = '" + oldusername + "' AND `password` = '" + oldPassword + "' AND `user_type_id` = '" + userType_id + "'"
                     );

                     if (resultSet.next()) {
                         // If valid, update the username and password in the database
                        MYSQL.execute("UPDATE `user` SET `username` = '" + newusername + "', `password` = '" + newPassword + "' WHERE `username` = '" + oldusername + "' AND `user_type_id` = '" + userType_id + "'");
                        JOptionPane.showMessageDialog(this, "Username and Password updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                         reset();
                     } else {
                         // If old username and password do not match, show an error message
                         JOptionPane.showMessageDialog(this, "Old Username or Password is incorrect", "Login Error", JOptionPane.ERROR_MESSAGE);
                     }

                 } catch (Exception e) {
                     e.printStackTrace();
                     JOptionPane.showMessageDialog(this, "An error occurred during the update process", "Error", JOptionPane.ERROR_MESSAGE);
                 }
           }


        
    }//GEN-LAST:event_jButton2ActionPerformed

